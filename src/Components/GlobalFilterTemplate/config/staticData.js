// Helper function to get data by page type and specific key
export const getStaticData = (pageType, specificKey = null) => {
  // console.log('getStaticData called with pageType:', pageType, 'specificKey:', specificKey);
  if (specificKey) {
    const result = staticData[pageType]?.[specificKey] || null;
    // console.log('getStaticData result:', result);
    return result;
  }
  const result = staticData[pageType] || null;
  // console.log('getStaticData result:', result);
  return result;
};

// Helper function to get FAQ data
export const getFAQData = (pageType, specificKey = null) => {
  const data = getStaticData(pageType, specificKey);
  if (data?.faqs) {
    return [...data.faqs, ...staticData.commonFAQs];
  }
  return staticData.commonFAQs;
};

