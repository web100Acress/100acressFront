// Hook to use SVG icons from sprite
export const useSVGIcon = (iconName, className = '', style = {}) => {
  return (
    <svg className={className} style={style}>
      <use href={`#icon-${iconName}`} />
    </svg>
  );
};

// Preload critical SVG icons
export const preloadSVGIcons = () => {
  const criticalIcons = [
    'youtube', 'facebook', 'instagram', 'linkedin', 'twitter',
    'phone', 'email', 'location', 'heart', 'share', 'search',
    'menu', 'close', 'arrow-right', 'arrow-left', 'star', 'home', 'building'
  ];

  criticalIcons.forEach(iconName => {
    const link = document.createElement('link');
    link.rel = 'preload';
    link.as = 'image';
    link.href = `#icon-${iconName}`;
    document.head.appendChild(link);
  });
};

// Optimize SVG loading
export const optimizeSVGLoading = () => {
  // Add loading="lazy" to SVG elements
  const svgs = document.querySelectorAll('svg:not([loading])');
  svgs.forEach(svg => {
    if (!svg.hasAttribute('loading')) {
      svg.setAttribute('loading', 'lazy');
    }
  });
};
